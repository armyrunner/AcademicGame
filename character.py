class Character: 
    
    def __init__(self,x,y,w,h):
        self.mX = x
        self.mY = y
        self.mW = w
        self.mH = h
    
    def getX():
        return self.mX
    
    def setX(x):
        self.mX = x
      
    def getY():
        return self.mY
    
    def setY(y):
        self.mY = y
      
    def getW():
        return self.mW
    
    def setW(w):
        self.mW = w
      
    def getH():
        return self.mH
    
    def setH(h):
        self.mH = h
        
        
        
