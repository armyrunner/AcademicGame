class character: 
    
    def __init__(self,x,y,w,h):
        self.mX = x
        self.mY = y
        self.mW = w
        self.mH = h
        return 

    def getX(self):
        return self.mX
    
    def setX(self,x):
        self.mX = x
        return
      
    def getY(self):
        return self.mY
    
    def setY(self,y):
        self.mY = y
        return
      
    def getW(self):
        return self.mW
    
    def setW(self,w):
        self.mW = w
        return
      
    def getH(self):
        return self.mH
    
    def setH(self,h):
        self.mH = h
        return
        
        
        
